package com.safetyplatform.entity;

public class EchartsData {

    private String name;
    private double value;

    public EchartsData() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}
